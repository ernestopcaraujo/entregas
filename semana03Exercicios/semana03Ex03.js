//executar no console do navegador

//a variável é declarada e ela recebe a atribuição de uma string a ser exibida para o usuário
var usuarioQuestao = "Qual seu nome ?";

//o window.prompt exibe o valor da variável usuarioQuestao ao usuário
//a variável meuNome é declarada e tem seu valor atribuído pelo usuário através do window.prompt()
var meuNome = window.prompt(usuarioQuestao);

//o window.alert exibe para o usuário o valor da variável meuNome
window.alert(meuNome);